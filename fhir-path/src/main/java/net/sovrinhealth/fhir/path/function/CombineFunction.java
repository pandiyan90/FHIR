package net.sovrinhealth.fhir.path.function;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

import net.sovrinhealth.fhir.path.FHIRPathNode;
import net.sovrinhealth.fhir.path.evaluator.FHIRPathEvaluator.EvaluationContext;

public class CombineFunction extends FHIRPathAbstractFunction {
    @Override
    public String getName() {
        return "combine";
    }

    @Override
    public int getMinArity() {
        return 1;
    }

    @Override
    public int getMaxArity() {
        return 1;
    }
    
    @Override
    public Collection<FHIRPathNode> apply(EvaluationContext evaluationContext, Collection<FHIRPathNode> context, List<Collection<FHIRPathNode>> arguments) {
        Collection<FHIRPathNode> combined = new ArrayList<>(context);
        combined.addAll(arguments.get(0));
        return combined;
    }
}
